using System;


class Program
{
    public static void Main()
    {
        // The next line declares an integer variable x and initializes it to 10
        int x = 10;
        Console.WriteLine("Value of x: " + x);

        // The next line declares a double variable r and initializes it to 3.55
        double r = 3.55;
        double pi = Math.Acos(-1);
        double c = 2 * pi * r;
        Console.WriteLine("Circumference of circle with radius " + r + " is: " + c);

        // The next line declares a character variable c and initializes it to 'A'
        char c = 'A';
        Console.WriteLine("Value of c: " + c);
        
        //boolean variables can be true or false
        bool a = true;

        Console.WriteLine("Value of a: " + a);

    }
}
